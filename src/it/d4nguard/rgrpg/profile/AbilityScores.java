package it.d4nguard.rgrpg.profile;

public class AbilityScores
{
    int strength;
    int dexterity;
    int constitution;
    int intelligence;
    int wisdom;
    int perception;
    int charisma;
    int appearance;
    int dialectic;
    int fortune;

    public AbilityScores()
    {
    }

    public int getStrength()
    {
	return this.strength;
    }

    public void setStrength(int value)
    {
	this.strength = value;
    }

    public int getDexterity()
    {
	return this.dexterity;
    }

    public void setDexterity(int value)
    {
	this.dexterity = value;
    }

    public int getConstitution()
    {
	return this.constitution;
    }

    public void setConstitution(int value)
    {
	this.constitution = value;
    }

    public int getIntelligence()
    {
	return this.intelligence;
    }

    public void setIntelligence(int value)
    {
	this.intelligence = value;
    }

    public int getWisdom()
    {
	return this.wisdom;
    }

    public void setWisdom(int value)
    {
	this.wisdom = value;
    }

    public int getPerception()
    {
	return this.perception;
    }

    public void setPerception(int value)
    {
	this.perception = value;
    }

    public int getCharisma()
    {
	return this.charisma;
    }

    public void setCharisma(int value)
    {
	this.charisma = value;
    }

    public int getAppearance()
    {
	return this.appearance;
    }

    public void setAppearance(int value)
    {
	this.appearance = value;
    }

    public int getDialectic()
    {
	return this.dialectic;
    }

    public void setDialectic(int value)
    {
	this.dialectic = value;
    }

    public int getFortune()
    {
	return this.fortune;
    }

    public void setFortune(int value)
    {
	this.fortune = value;
    }
}
